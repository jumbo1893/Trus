package com.jumbo.trus.statistics.player;

public class ListTexts {

    private final String title;
    private final String text;

    public ListTexts(String title, String text) {
        this.title = title;
        this.text = text;
    }

    public String getTitle() {
        return title;
    }

    public String getText() {
        return text;
    }
}
